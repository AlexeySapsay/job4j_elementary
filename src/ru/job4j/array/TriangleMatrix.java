package ru.job4j.array;

public class TriangleMatrix {
    public static int[][] rows(int count) {
        int[][] triangle = new int[count][];
        for (int i = 0; i < count; i++) {
            for (int j = 0; j < count + i; j++) {
                System.out.println("hi!");
            }
        }
        return triangle;
    }
}

